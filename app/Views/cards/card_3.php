                    <h4>Requisitos de Projeto</h4>
                    <p>Para realizar o levantamento de requisitos de projeto a proteção de dados, por padrão, necessita de um desenvolvimento voltado para a minimização:</p>
                    <ul>
                        <li>Coleta de uma quantidade mínima dados pessoais, ou seja, somente os realmente necessários, tanto no <i>front-end</i> quanto no <i>back-end</i>;</li>
                        <li>Não vinculação dos dados pessoais a outros grupos de dados armazenados em um único local;</li>
                        <li>Remoção de dados pessoais e de identificação no caso de agregação de dados.</li>
                    </ul>
                    <p>Embora as diretrizes que compõem o  <i>General Data Protection Regulation</i> (GDPR), a qual a LGPD se baseia, exijam a definição de agendas de retenção e exclusão para todos seus dados pessoais, não há nenhuma obrigatoriedade que você exclua tudo como regra. Você pode muito bem manter registradas informações de compras por 5, 10 anos para fins fiscais. Desde que tudo seja documentado e justificado, manter os dados é aceitável.</p>
                    <p>Vale ressaltar que a exclusão dos dados, quando estes não forem mais necessários, pode ser feita tanto automaticamente quanto por meio de ações do usuário.</p>
                    <p>Outro aspecto do design do sistema está ligado a privacidade. Dessa forma, dados pessoais não devem estar à vista de todos, nem no frontend e nem no <i>back-end</i>. Além disso não é recomendado que todos os usuários tenham acesso universal.</p>
                    <p>Por questões de segurança e privacidade, os dados do usuário precisam estar criptografados, estejam eles em trânsito ou em repouso.</p>
