                    <h4>Requisítos Mínimos para Atender a LGPD</h4>
                    <p>Para a devida adequação do site às exigências da Lei, é preciso trabalhar os passos a seguir:</p>
                    <ul>
                        <li><strong>Segurança de tráfego:</strong> <i>Secure Sockets Layer</i> (SSL); todos os projetos que trafegam dados de usuários devem ter essas informações criptografas através da instalação de um Certificado de Segurança Digital;</li>
                        <li><strong>Formatação da Política de Privacidade:</strong> texto contendo todos os tipos de dados, as formas de captação, os tipo de uso e as maneiras como o usuário pode ter informação, assim como domínio em cima de seus dados-pessoais armazenados;</li>
                        <li><strong>Mensagem de aceite:</strong> uma mensagem deve ser exibida em todas as páginas para que o usuário aceite os termos de sua Política de Privacidade;</li>
                        <li><strong>Canal de comunicação:</strong> sistema para o cliente solicitar a alteração ou exclusão de seus dados pessoais no site.</li>
                    </ul>
                    <p>Vale ressaltar que essa adequação visa atender as exigências da lei, porém sua não conformidade também pode pesar seriamente em cima da reputação do negócio, então é preciso se adaptar e entender que as regras em cima do universo online ainda estão em formação é preciso se manter informado.</p>
