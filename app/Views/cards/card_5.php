                    <h4>Anonimização</h4>
                    <p>A anonimização é uma técnica de processamento de dados que remove ou modifica informações que possam identificar uma pessoa de forma definitiva. Essa técnica resulta em dados anonimizados, que não podem ser associados a nenhum indivíduo específico.</p>
                    <p>Nesse sentido, a anonimização ocorre com a remoção de informações de um documento, de forma que os dados ali contidos não são capazes de serem  vinculados a uma pessoa e, consequentemente, não são mais protegidos pela LGPD.</p>
                    <p>Portanto, se após o ciclo de vida de determinado dado, a intenção do Controlador seja a de armazenar esses dados, uma medida que pode ser adotada para viabilizar esse armazenamento é a anonimização desse dado.</p>
                    <p></p>
