                <div>
                    <h4>Pseudonimização</h4>
                    <p>A pseudonimização, por sua vez, é uma técnica que substitui informações contidas num conjunto de dados que identifica um indivíduo por um outro identificador artificial, um pseudônimo.</p>
                    <p>Segundo consta na LGPD, "a pseudonimização é o tratamento por meio do qual um dado perde a possibilidade de associação, direta ou indireta, a um indivíduo, senão pelo uso de informação adicional mantida separadamente pelo controlador em ambiente controlado e seguro." (BRASIL, 2018 p. 7).</p>
                    <p>Na pseudonimização, os dados pessoais são substituídos por esse identificador artificial e mantidos num banco de dados separado que liga dados pessoais e esse pseudônimo.</p>
                    <p>Como exemplo de dados pseudoanonimizados, pode haver um sistema de controle biométrico, no qual o funcionário pode ser identificado por um número ou código, ao invés de ter seu nome registrado.</p>
                    <p>Portanto, a pseudoanonimização também é uma importante técnica que pode e deve ser usada pelas empresas no processo de adequação, a fim de dar maior segurança ao tratamento de dados pessoais.</p>
